package BitwiseOperator;

public class BitwiseOperatorDemo {

	public static void main(String[] args) {
    int n = 60;
    System.out.println("left shift "+ (n<<2)); //left shift
    System.out.println("right shift "+(n>>2)); //right shift
    System.out.println("zero fill right shift "+(n>>>2));//zero fill right shift
    System.out.println();
    System.out.println();
	}

}
